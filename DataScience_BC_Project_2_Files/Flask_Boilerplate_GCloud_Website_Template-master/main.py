@app.route('/api/tickerdata/<tickername>')
def tickerdata(tickername =""): 
    scraped_df = pd.read_csv("static/data/tickerdata.csv")
    filtered_df = scraped_df[scraped_df["ticker"]==tickername.upper()].reset_index()
    return jsonify(filtered_df.to_dict())

